def convertekm():
    kmh = int(input("Qual a velocidade em Km/h: "))
    mps = kmh / 3.6
    print(f'A velocidade em m/s é: {mps} ')
convertekm()

def converteCelsius():
    cel = int(input("Qual a velocidade em Km/h: "))
    far = (cel*1.8)+32
    print(f'a temperatura em Farenheit é: {far}')
converteCelsius()
